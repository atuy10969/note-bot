import anthropic
from config import ANTHROPIC_API_KEY
from note_poster import post_to_note

client = anthropic.Anthropic(api_key=ANTHROPIC_API_KEY)

prompt = """以下の構成でnote記事の本文を書いてください。

【ライタープロフィール】
・22歳・理系大学4年生
・4工大（偏差値高くない理系大学）出身、大手金融に内定済み
・就活界隈では「勝ち組」と言われる金融業界に、学歴ハンデを乗り越えて内定
・文体：親しみやすい語りかけ調、難しい言葉は使わない
・一人称：僕

【記事タイトル】
偏差値高くない理系大学から大手金融に内定した僕が、サマーインターン前にやっておけばよかったこと3選

【記事の核心メッセージ】
学歴じゃなくて、動き出しの早さで就活は決まる。
偏差値が高くなくても、理系でも、戦略と行動次第で大手金融は狙える。

【目次・構成の形式について】
記事冒頭に以下の形式で目次を作成してください。

---
📋 目次
① 逆求人サービスに登録する
② 自己分析を言語化する
③ OB訪問を1件だけやってみる
---

各章の見出しはH2（##）で「① 逆求人サービスに登録する」のように番号付きで統一してください。

【記事の構成】

■イントロ
・「4工大から大手金融？」という意外性から入る
・学歴コンプレックスがあった自分の話を少し入れて共感を取る
・「早く動いたことが全てだった」という結論を最初に見せる

■## ① 逆求人サービスに登録する
・学歴フィルターを回避できる最大の武器として紹介
・待つだけで企業からオファーが来る仕組みの説明
・理系スキルは金融でも需要が高いという実体験
・【アフィリエイトリンク挿入予定】と記載しておく

■## ② 自己分析を言語化する
・学歴で勝てないなら、中身で勝つという視点
・ESは早く書き始めるほど質が上がる理由
・ガクチカの作り方を具体的に（研究・バイト・サークルなど理系あるある）
・【アフィリエイトリンク挿入予定】と記載しておく

■## ③ OB訪問を1件だけやってみる
・学歴が不利な分、人との繋がりで差をつける戦略
・ネットの情報と現場の情報の差を伝える
・やり方・探し方を具体的に（Matcher・ビズリーチキャンパスなど）
・【アフィリエイトリンク挿入予定】と記載しておく

■まとめ
・「学歴じゃなく、行動が全てを変えた」というメッセージで締める
・同じ境遇の読者へのエール
・フォローを促す一言

【文字数】1500〜2000文字程度

【注意点】
・です・ます調ではなく、語りかける柔らかいタメ口寄りの文体
・難しい就活用語には簡単な説明を添える
・学歴を言い訳にしない、前向きなトーンを保つ
・自慢っぽくならず、等身大のリアルさを大事に
・実体験ベースで書く（盛りすぎない）
・アフィリエイトリンクは【アフィリエイトリンク挿入予定】というプレースホルダーで残しておく
・マークダウン形式で出力する"""

print("記事生成中...")
message = client.messages.create(
    model="claude-haiku-4-5-20251001",
    max_tokens=4000,
    messages=[{"role": "user", "content": prompt}]
)

body = message.content[0].text
title = "偏差値高くない理系大学から大手金融に内定した僕が、サマーインターン前にやっておけばよかったこと3選"
print(f"生成完了！文字数: {len(body)}字")
print(f"\n--- 冒頭200字 ---\n{body[:200]}\n---\n")

print("投稿中...")
result = post_to_note(title, body, publish=True)
print("結果:", "成功" if result else "失敗")
